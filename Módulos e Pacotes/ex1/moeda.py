def metade(valor):
    preco = valor / 2
    return preco # Retorna o preco

def dobro(valor):
    preco = valor * 2
    return preco # Retorna o preco

def aumentar(valor):
    preco = valor + (valor * 10 / 100)
    return preco # Retorna o preco

def diminuir(valor):
    preco = valor - (valor * 15/100)
    return preco # Retorna o preco
